# 3)Сложите все числа в списке, они могут быть отрицательными, если список пустой вернуть 0
# [] == 0
# [1, 2, 3] == 6
# [1.1, 2.2, 3.3] == 6.6
# [4, 5, 6] == 15
# range(101) == 5050

list1 = [-10, 6, 627, -90, 15]
if list1 == []:
    print(0)
else:
    sum1 = sum(list1)
    print(f'{list1} == {sum1}')
